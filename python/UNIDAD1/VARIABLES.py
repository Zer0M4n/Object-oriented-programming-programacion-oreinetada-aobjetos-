#Para declarar variables siempre se debe empezar con una letra 
#es una sintaxis ilegal si se llega a empezar desde un numero

Numero = 1
print(Numero)
#Output :  "1"

#Tambien diferentes variables se pueden relacionar con una variable y asu vez estar en una sola linea

a = b = c = "PEDRO"

print(a,b,c)

#Output : "PEDRO PEDRO PEDRO"

#A su vez se puede declarar 3 variables en una sola linea
x,y,z = 4,2,3
print(x,y,z)
#Output : "4 2 3"

